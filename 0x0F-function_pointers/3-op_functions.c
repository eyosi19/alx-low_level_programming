#include "3-calc.h"

/**
 * op_add - add a, b
 *
 * @a: number 1
 *
 * @b: number 2
 *
 * Return: the sum of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub a, b
 *
 * @a: number 1
 *
 * @b: number 2
 *
 * Return: the sub of a and b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - mul a, b
 *
 * @a: number 1
 *
 * @b: number 2
 *
 * Return: the mul of a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - div a, b
 *
 * @a: number 1
 *
 * @b: number 2
 *
 * Return: the div of a and b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - mod a, b
 *
 * @a: number 1
 *
 * @b: number 2
 *
 * Return: the mod of a and b
 */

int op_mod(int a, int b)
{
	return (a % b);
}

